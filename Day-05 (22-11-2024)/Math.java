
public class Math {
    public int add(int x, int y) {
        return x+y;
    }
    public int subtract(int x, int y) {
        return x-y;
    }
    public double multiply(int x, int y) {
        return x*y;
    }
    public double divide(int x, int y) {
        return x/y;
    }
}
